package com.example.healthy;

public class Info {
    public String height;
    public String weight;
    public String temp;
    public String heart_rate;
    public String high_pre;
    public String low_pre;
    public String blood_sugar;
    public String randText;
    public String message_box;


    public String getRandText() {
        return randText;
    }

    public void setRandText(String randText) {
        this.randText = randText;
    }

    public String getMessage_box() {
        return message_box;
    }

    public void setMessage_box(String message_box) {
        this.message_box = message_box;
    }

    public String getBlood_sugar() {
        return blood_sugar;
    }

    public void setBlood_sugar(String blood_sugar) {
        this.blood_sugar = blood_sugar;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getTemp() {
        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getHeart_rate() {
        return heart_rate;
    }

    public void setHeart_rate(String heart_rate) {
        this.heart_rate = heart_rate;
    }

    public String getHigh_pre() {
        return high_pre;
    }

    public void setHigh_pre(String high_pre) {
        this.high_pre = high_pre;
    }

    public String getLow_pre() {
        return low_pre;
    }

    public void setLow_pre(String low_pre) {
        this.low_pre = low_pre;
    }
}
